import { Schema, model } from 'mongoose';

const usersSchema = new Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Users = model('Users', usersSchema);

export default Users;
